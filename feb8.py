#  dictionary

mydict = {'name' : "islam",
          'class' : "mca",
          'year' : 1}

print(mydict['name']) #prints islam
print(len(mydict))   # prints the length of dictionary
print(mydict.keys())    # prints all the keys 
print(mydict.values())  # prints all the values
print(type(mydict))  # prints type of dictionary